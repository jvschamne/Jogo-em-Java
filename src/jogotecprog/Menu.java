    private void botaoCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCarregarActionPerformed
        //ler arquivo

        String nomeJog1 = null, nomeJog2 = null;
        int vidaJog1 = 0, vidaJog2 = 0, fase = 0;

        String linhas[] = new String[5];

        SaveLoad carregarTxt = new SaveLoad();
        linhas = carregarTxt.carregar("save.txt");
        /*a lista de strings linhas recebe a lista de strings que o metodo
        carregar retorna*/

        nomeJog1 = linhas[0];
        nomeJog2 = linhas[1];
        vidaJog1 = Integer.parseInt(linhas[2]);
        vidaJog2 = Integer.parseInt(linhas[3]);
        fase = Integer.parseInt(linhas[4]);

        /*passando esses dados salvo para comecar o jogo com eles */
        contr.setNomeDuo(nomeJog1, nomeJog2, fase, vidaJog1, vidaJog2);
        contr.comecaJogo();
        this.setVisible(false);

    }//GEN-LAST:event_botaoCarregarActionPerformed

