    private void botaoRecordeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRecordeActionPerformed
        String linhas[] = new String[100];

        SaveLoad carregarRecorde = new SaveLoad();
        linhas = carregarRecorde.carregarRecordes("recorde.txt");
        //linhas = carregarRecorde.carregarRecordes("recorde.csv");

        /*a lista de strings linhas recebe a lista de strings que o metodo
        carregarRecordes retorna*/
        String recorde = "";

        Recordes janelaRecordes = new Recordes();
        janelaRecordes.setVisible(true);
        janelaRecordes.setSize(600, 500);
        janelaRecordes.setResizable(false);

        for (int c = 0; c < 100; c++) {
            if (linhas[c] != null) {
                recorde = linhas[c];
                janelaRecordes.getTabela().setValueAt(recorde, c, 0);
            }
        }

    }//GEN-LAST:event_botaoRecordeActionPerformed

